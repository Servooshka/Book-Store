// Функция, которая будет вызвана после полной загрузки API Яндекс.Карт
function init() {
    // 1. Создание экземпляра карты и его привязка к контейнеру 'map'.
    // Первые параметры: [широта, долгота] - центр карты.
    // Второй параметр: уровень масштабирования (от 0 до 19).
    var myMap = new ymaps.Map("map", {
        center: [55.76, 37.64], // Координаты центра: Москва
        zoom: 10
    }, {
        // Опции карты (например, отключение скролла для масштабирования)
        searchControlProvider: 'yandex#search'
    });

    // 2. Добавление метки на карту (опционально)
    var myPlacemark = new ymaps.Placemark([55.76, 37.64], {
        // Данные, которые отображаются во всплывающей подсказке
        balloonContent: '<strong>Наш офис</strong>'
    }, {
        // Опции метки
        preset: 'islands#icon',
        iconColor: '#0095b6'
    });

    // 3. Добавление метки на карту
    myMap.geoObjects.add(myPlacemark);
}

// Убедитесь, что функция init() вызовется после загрузки модуля 'map'
ymaps.ready(init);